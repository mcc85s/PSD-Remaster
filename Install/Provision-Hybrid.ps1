                [ System.Management.Automation.Credential () ] $Creds )

        ( $Domain , $Root , $Username , $Password ) = @( 0..3 | % { $Null } )
     
        Try       
        {
            $AD      = "LDAP://$( ( [ ADSI ]'').distinguishedName )"
            $Creds   | % { $UN = $_.Username ; $PW = $_.GetNetworkCredential().Password } 
