                GuiStart 
            } 
            
            Else
            { 
                Black_Viper_Set $Black_Viper $All_or_Min 
            }

		} 

        ElseIf ( $TOS -eq 'l' )
        {
			$CR      = $True
            ShowCopyright
		} 
        
        Else 
        {
			$Invalid = $True
		}

	} 

    Return
