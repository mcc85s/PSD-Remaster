            If ( $var -eq $tmp[$i+1] )
            { 
                $TruCount++ 
            }
		}

		If ( $TruCount -eq $Count )
        { 
            $Script:Uparg += $_.Arg + " "
        }
	}
	
    If ( $ScriptLog -eq 1 )
    { 
        $UpArg            += "-logc $LogName " 
