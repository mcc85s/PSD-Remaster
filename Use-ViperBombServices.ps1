			$T2 = While ( $Tmp )
            { 
                $Key, $Val, $Tmp = $Tmp

                [ PSCustomObject ]@{
                
                    Name = $Key
                    Val  = $Val 
                } 
            }
			
            If ( Compare $T1 $T2 -Property Name , Val )
            { 
                $SaveSettingFile = $True 
            }

        } 
        
        Else 
        {
			$SaveSettingFile = $True
		}
	} 

    Else 
    {
		$SaveSettingFile = $True
	}

	If ( $SaveSettingFile )
    { 
        $Settings | Export-Clixml -LiteralPath $SettingPath 
    }
}

