        { 
            $Key , $Val , $Tmp = $Tmp
            SV $Key $Val -Scope Script 
        }
	}

	$Script:IsLaptop = If ( $PCType -eq 1 ) { '-Desk' } 

    Else 
    { 
        '-Lap'
        If ( $LaptopTweaked -ne 1 -and $Black_Viper -ge 2 )
